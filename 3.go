package main

import "fmt"

func main() {
	integerVar := 11
	floatVar := 3.14
	stringVar := "Hello world"
	boolVar := true

	fmt.Println("Целочисленное значение:", integerVar)
	fmt.Println("Число с плавающей запятой:", floatVar)
	fmt.Println("Строка:", stringVar)
	fmt.Println("Логическое значение:", boolVar)
}
